/* --------------------------------------------------------------------------------------------------
 * クラス定義
 *    export class クラス名 { }
 *    ※exportを付けると、他クラスから参照(import)可能となる
 * ------------------------------------------------------------------------------------------------ */

export class Technology {
  /* --------------------------------------------------------------------------------------------------
  * フィールド定義
  *    フィールド名: 型名;
  *    ※使用できる主な型は、
  *     ・any       型指定なし
  *     ・number    数値(浮動小数含む)
  *     ・boolean   真偽値
  *     ・string    文字列
  *     ・enum      列挙
  *    ※配列は、型[]／Array<型> で指定
  *    ※タプルは、[型, 型, …]
  * ------------------------------------------------------------------------------------------------ */

  id: number;
  name: string;
  remark: string;
}
